# area of trapezoid calculator

print("This calculates the area of a trapezoid\n")

h = float(input("Enter the height: "))
lb = float(input("Enter the length of the bottom base: "))
lt = float(input("Enter the length of the top base: "))

A = 0.5 * (lb + lt) * h

print("\nThe area of the trapezoid is", A,)
input("\n\nPress the enter key to exit")
